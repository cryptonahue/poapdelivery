const months = (index) => {
	let month = "";

	if (index == 1) {
		month = "Enero";
	}
	if (index == 2) {
		month = "Febrero";
	}
	if (index == 3) {
		month = "Marzo";
	}
	if (index == 4) {
		month = "Abril";
	}
	if (index == 5) {
		month = "Mayo";
	}
	if (index == 6) {
		month = "Junio";
	}
	if (index == 7) {
		month = "Julio";
	}
	if (index == 8) {
		month = "Agosto";
	}
	if (index == 9) {
		month = "Septiembre";
	}
	if (index == 10) {
		month = "Octubre";
	}
	if (index == 11) {
		month = "Noviembre";
	}
	if (index == 12) {
		month = "Diciembre";
	}

	return month;
};

export default months;
